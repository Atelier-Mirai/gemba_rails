# 配列を生成する
[1, 2, 3]

["abc", false, nil, 4, [1, 2, 3]]

a = [1, 2, 3]
# 1, 2, 3と順番に出力する
a.each do |element|
  puts element
end

a = [1, 2, 3]
a << 4
puts a
#=> [1, 2, 3, 4]

a[0]
#=> 1
a[1]
#=> 2
a[2]
#=> 3
a[3]
#=> 4
a[4]
#=> nil

a[-1]
#=> 4

a[0] = 10
puts a
# => [10, 2, 3, 4]

a[5] = 50
puts a
# => [10, 2, 3, 4, nil, 50]
